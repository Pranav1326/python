'''
(&) bitwise AND
(|) bitwise OR
(~) bitwise NOT
(^) bitwise XOR
(>>) shift right
(<<) shift left
'''

a = 10
b = 4

# bitwise AND operator
print("a & b =", a&b)

# bitwise OR operator
print("a | b =", a|b)

# bitwise NOT operator
print("~a = ", ~a)

# bitwise XOR operator
print("a ^ b =", a^b)

# shift right operator
print("a >> 2", a>>2)

# shift left operator
print("a << 2", a<<2)
