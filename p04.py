# Python program for Assignment operators (=, +=, -=, *=, /=, %=)
# By : Pranav Visavadia

# = operator
n = 10
print(n)

# += operator
n += 10
print(n)

# -= operator
n -= 15
print(n)

# *= operator
n *= 6
print(n)

# /= operator
n *= 3
print(n)

# %= operator
n %= 4
print(n)
